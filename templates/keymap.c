static bool precision = false;
static uint16_t precision_mod = 64;
// static uint16_t axis_val = 127;
static uint16_t axis_val = 255;

uint8_t mod_state;
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (!process_achordion(keycode, record)) return false;
  if (!process_layer_lock(keycode, record, PG_LOCK)) return false;
  if (!process_select_word(keycode, record, PG_SEL)) return false;

  switch (get_highest_layer(default_layer_state)) {
    case _ERMAK:
    case _TYPING:
      if (!process_custom_shift_keys(keycode, record)) return false;
  }

  int16_t precision_val = axis_val;
  if (precision) {
    precision_val -= precision_mod;
  }

  switch (keycode) {
    // keys
    case KC_CLEAR:
      if (!record->event.pressed) return false;
      clear_mods();
      break;
    case KC_RSPC:
      if (!record->event.pressed) return false;
      tap_code16(KC_SPACE);
      tap_code16(KC_LEFT);
      break;

    // modifiers
    case LSFT_T(PG_LOCK):
    case RSFT_T(PG_LOCK):
      if (record->event.pressed && record->tap.count) {
        layer_lock_invert(get_highest_layer(layer_state));
        return false;
      }
      break;

    // layers
    case LT(_NUM, BASE):
    case LT(_NAV, BASE):
      if (record->event.pressed && record->tap.count) {
        default_layer_set(_ERMAK);
        layer_move(_ERMAK);
        return false;
      }
      break;

    // lingers
    case L(KC_LEFT_BRACKET):
      if (!record->event.pressed) return false;
      if (record->tap.count) {
        tap_code16(KC_LEFT_BRACKET);
      } else {
        tap_code16(KC_LEFT_BRACKET);
        tap_code16(KC_RIGHT_BRACKET);
        del_mods(MOD_MASK_SHIFT);
        tap_code16(KC_LEFT);
      }
      return false;
    case L(KC_RIGHT_PAREN):
      // TODO: clean up. Using `l[)]` sucks, and so does manually custom shifting
      if (!record->event.pressed) return false;
      if (record->tap.count) {
        if (get_mods() & MOD_MASK_SHIFT) {
          tap_code16(KC_RIGHT_PAREN);
        } else {
          tap_code16(KC_DOUBLE_QUOTE);
        }
      } else {
        if (get_mods() & MOD_MASK_SHIFT) {
          tap_code16(KC_RIGHT_PAREN);
        } else {
          tap_code16(KC_DOUBLE_QUOTE);
          tap_code16(KC_DOUBLE_QUOTE);
          del_mods(MOD_MASK_SHIFT);
          tap_code16(KC_LEFT);
        }
      }
      return false;
    case L(KC_QUOTE):
      if (!record->event.pressed) return false;
      if (record->tap.count) {
          tap_code16(KC_QUOTE);
      } else {
        if (get_mods() & MOD_MASK_SHIFT) {
          tap_code16(KC_LEFT_PAREN);
          tap_code16(KC_RIGHT_PAREN);
          del_mods(MOD_MASK_SHIFT);
          tap_code16(KC_LEFT);
        } else {
          tap_code16(KC_QUOTE);
          tap_code16(KC_QUOTE);
          del_mods(MOD_MASK_SHIFT);
          tap_code16(KC_LEFT);
        }
      }
      return false;
    case L(KC_GRAVE):
      if (!record->event.pressed) return false;
      if (record->tap.count) {
        tap_code16(KC_GRAVE);
      } else {
        tap_code16(KC_GRAVE);
        tap_code16(KC_GRAVE);
        del_mods(MOD_MASK_SHIFT);
        tap_code16(KC_LEFT);
        return true;
      }
      return false;
    case L(KC_Q):
      if (!record->event.pressed) return false;
      if (is_caps_word_on()) add_weak_mods(MOD_MASK_SHIFT);
      tap_code16(KC_Q);
      if (!record->tap.count) {
        del_mods(MOD_MASK_SHIFT);
        tap_code16(KC_U);
      }
      return false;

    // apps
    case LGUI_T(KC_NO):
    case RGUI_T(KC_NO):
      if (record->event.pressed && record->tap.count) {
        // hyprland
        tap_code16(LAG(KC_SPACE));
        return false;
      }
      break;
    case LALT_T(KC_NO):
    case RALT_T(KC_NO):
      if (record->event.pressed && record->tap.count) {
        // wezterm
        tap_code16(C(KC_SPACE));
        return false;
      }
      break;

    // joystick
    case KC_JUP:
      joystick_set_axis(1, record->event.pressed ? precision_val : 0);
      return false;
    case KC_JDOWN:
      joystick_set_axis(1, record->event.pressed ? -precision_val : 0);
      return false;
    case KC_JLEFT:
      joystick_set_axis(0, record->event.pressed ? -precision_val : 0);
      return false;
    case KC_JRIGHT:
      joystick_set_axis(0, record->event.pressed ? precision_val : 0);
      return false;
    case KC_JMOD:
      precision = record->event.pressed;
      return false;
    case KC_CUP:
      joystick_set_axis(3, record->event.pressed ? precision_val : 0);
      return false;
    case KC_CDOWN:
      joystick_set_axis(3, record->event.pressed ? -precision_val : 0);
      return false;
    case KC_CLEFT:
      joystick_set_axis(2, record->event.pressed ? -precision_val : 0);
      return false;
    case KC_CRIGHT:
      joystick_set_axis(2, record->event.pressed ? precision_val : 0);
      return false;
  }

  return true;
}

